def decode(loc : Tensor, priors : Tensor, variances : Tensor) -> Tensor:
    """Decode locations from predictions using priors to undo
    the encoding we did for offset regression at train time.
    Args:
        loc (tensor): location predictions for loc layers,
            Shape: [num_priors,4]
        priors (tensor): Prior boxes in center-offset form.
            Shape: [num_priors,4].
        variances: (list[float]) Variances of priorboxes
    Return:
        decoded bounding box predictions
    """
    assert loc.dim() == 2
    assert loc.size(1) == 4
    assert priors.dim() == 2
    assert priors.size(1) == 4
    boxes = torch.cat((
        priors[:, :2] + loc[:, :2] * variances[0] * priors[:, 2:],
        priors[:, 2:] * torch.exp(loc[:, 2:] * variances[1])), 1)
    lt = boxes[:, :2] - boxes[:, 2:] / 2
    rb = boxes[:, 2:] + lt
    return torch.cat((lt,rb),dim=1)

def decode_landm(pre : Tensor, priors : Tensor, variances : Tensor, n_landmarks : int=5) -> Tensor:
    """Decode landm from predictions using priors to undo
    the encoding we did for offset regression at train time.
    Args:
        pre (tensor): landm predictions for loc layers,
            Shape: [num_priors,10]
        priors (tensor): Prior boxes in center-offset form.
            Shape: [num_priors,4].
        variances: (list[float]) Variances of priorboxes
    Return:
        decoded landm predictions
    """
    landmarks = []
    for i in range(0,n_landmarks*2,2) :
        landmarks.append(priors[:, :2] + pre[:, i:i+2] * variances[0] * priors[:, 2:])
    landmarks = torch.cat(landmarks, dim=1)
    # landms = torch.cat((priors[:, :2] + pre[:, :2] * variances[0] * priors[:, 2:],
    #                     priors[:, :2] + pre[:, 2:4] * variances[0] * priors[:, 2:],
    #                     priors[:, :2] + pre[:, 4:6] * variances[0] * priors[:, 2:],
    #                     priors[:, :2] + pre[:, 6:8] * variances[0] * priors[:, 2:],
    #                     priors[:, :2] + pre[:, 8:10] * variances[0] * priors[:, 2:],
    #                     ), dim=1)
    return landmarks


def log_sum_exp(x):
    """Utility function for computing log_sum_exp while determining
    This will be used to determine unaveraged confidence loss across
    all examples in a batch.
    Args:
        x (Variable(tensor)): conf_preds from conf layers
    """
    x_max = x.data.max()
    return torch.log(torch.sum(torch.exp(x-x_max), 1, keepdim=True)) + x_max
